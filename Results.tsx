import React, { useEffect, useState, useRef } from 'react';
import { IATSessionData } from '../types';
import { calculateDScore, saveResultsToStorage, downloadResultsAsJSON } from '../services/storageService';
import { getAIAnalysis } from '../services/geminiService';
import { Loader2, Download, Save, RefreshCw, BrainCircuit, Check, AlertCircle, Database, Copy, User } from 'lucide-react';
import { BarChart, Bar, XAxis, YAxis, Tooltip, ResponsiveContainer, Cell } from 'recharts';

interface ResultsProps {
  data: IATSessionData;
  onRestart: () => void;
}

const Results: React.FC<ResultsProps> = ({ data, onRestart }) => {
  const [dScore, setDScore] = useState<number>(0);
  const [saving, setSaving] = useState(false);
  const [saved, setSaved] = useState(false);
  const [saveError, setSaveError] = useState<string | null>(null);
  const [needsKey, setNeedsKey] = useState(false);
  const [manualKey, setManualKey] = useState('');
  
  const [aiAnalysis, setAiAnalysis] = useState<string | null>(null);
  const [loadingAI, setLoadingAI] = useState(false);
  const [showSqlHelp, setShowSqlHelp] = useState(false);
  const [copiedSql, setCopiedSql] = useState(false);

  // Prevent double saving in Strict Mode
  const hasAutoSaved = useRef(false);

  useEffect(() => {
    // 1. Calculate Score
    const score = calculateDScore(data.results);
    setDScore(score);
    data.dScore = score;

    // 2. Auto Save Logic
    const autoSave = async () => {
        if (hasAutoSaved.current) return;
        hasAutoSaved.current = true;

        setSaving(true);
        setSaveError(null);
        
        const result = await saveResultsToStorage(data);
        
        setSaving(false);
        if (result.success) {
          setSaved(true);
        } else {
          setSaveError(result.message || 'Ошибка автоматического сохранения');
          if (result.needsKey) setNeedsKey(true);
          
          if (result.message?.includes('таблица') || result.message?.includes('RLS') || result.message?.includes('policy') || result.message?.includes('42501')) {
            setShowSqlHelp(true);
          }
        }
    };

    autoSave();
  }, [data]);

  const handleManualSave = async () => {
    setSaving(true);
    setSaveError(null);
    setNeedsKey(false);
    
    const result = await saveResultsToStorage(data, manualKey);
    
    setSaving(false);
    if (result.success) {
      setSaved(true);
      setShowSqlHelp(false);
    } else {
      setSaveError(result.message || 'Ошибка сохранения');
      if (result.needsKey) {
        setNeedsKey(true);
      }
      if (result.message?.includes('таблица') || result.message?.includes('RLS') || result.message?.includes('policy') || result.message?.includes('42501')) {
        setShowSqlHelp(true);
      }
    }
  };

  const handleAIAnalysis = async () => {
    setLoadingAI(true);
    const analysis = await getAIAnalysis(dScore);
    setAiAnalysis(analysis);
    setLoadingAI(false);
  };

  const sqlCode = `-- 1. Создание таблицы (обновлено для ID участника)
create table if not exists iat_results (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  session_id text,
  participant_id text,
  d_score numeric,
  full_data jsonb
);

-- 2. Включение защиты RLS
alter table iat_results enable row level security;

-- 3. Удаление старых политик (во избежание конфликтов)
drop policy if exists "Enable insert for everyone" on iat_results;
drop policy if exists "Enable select for everyone" on iat_results;

-- 4. РАЗРЕШИТЬ ЗАПИСЬ всем (anon/public)
create policy "Enable insert for everyone" 
on iat_results for insert 
to public 
with check (true);

-- 5. РАЗРЕШИТЬ ЧТЕНИЕ всем
create policy "Enable select for everyone" 
on iat_results for select 
to public 
using (true);`;

  const copySql = () => {
    navigator.clipboard.writeText(sqlCode);
    setCopiedSql(true);
    setTimeout(() => setCopiedSql(false), 2000);
  };

  // Prepare chart data
  const chartData = [
    { name: 'Ваш D-Score', score: Number(dScore.toFixed(2)) },
  ];

  return (
    <div className="flex flex-col items-center justify-center min-h-full p-8 space-y-8 animate-fade-in max-w-4xl mx-auto">
      <h2 className="text-4xl font-bold text-gray-800">Тест Завершен</h2>
      
      {/* Participant Info Banner */}
      <div className="bg-blue-50 text-blue-800 px-4 py-2 rounded-full text-sm font-medium flex items-center">
         <User size={14} className="mr-2" /> Участник ID: {data.participantId}
      </div>
      
      <div className="grid grid-cols-1 md:grid-cols-2 gap-8 w-full">
        {/* Score Card */}
        <div className="bg-white p-8 rounded-2xl shadow-lg border border-gray-100 flex flex-col items-center justify-center space-y-4">
          <h3 className="text-lg font-medium text-gray-500 uppercase tracking-wide">Ваш результат (D-Score)</h3>
          <div className={`text-6xl font-black ${dScore > 0 ? 'text-blue-600' : 'text-indigo-600'}`}>
            {dScore.toFixed(3)}
          </div>
          <p className="text-center text-gray-500 text-sm">
            Чем больше число отличается от нуля, тем сильнее ассоциация.
          </p>
          
          <div className="w-full h-32 mt-4">
            <ResponsiveContainer width="100%" height="100%">
               <BarChart data={chartData} layout="vertical">
                 <XAxis type="number" domain={[-2, 2]} hide />
                 <YAxis type="category" dataKey="name" hide />
                 <Tooltip />
                 <Bar dataKey="score" fill="#4f46e5" radius={[0, 4, 4, 0]}>
                    <Cell fill={dScore > 0 ? '#2563eb' : '#9333ea'} />
                 </Bar>
               </BarChart>
            </ResponsiveContainer>
          </div>
        </div>

        {/* Actions Card */}
        <div className="bg-white p-8 rounded-2xl shadow-lg border border-gray-100 flex flex-col space-y-4">
           <h3 className="text-lg font-medium text-gray-800">Данные и Анализ</h3>
           
           <button 
             onClick={handleAIAnalysis}
             disabled={loadingAI || aiAnalysis !== null}
             className="flex items-center justify-center px-4 py-3 bg-gradient-to-r from-purple-500 to-indigo-600 text-white rounded-lg hover:from-purple-600 hover:to-indigo-700 disabled:opacity-50 transition-all"
           >
             {loadingAI ? <Loader2 className="animate-spin mr-2"/> : <BrainCircuit className="mr-2"/>}
             {aiAnalysis ? 'Анализ готов' : 'Получить анализ ИИ'}
           </button>

           <div className="flex space-x-2">
             <button 
               onClick={handleManualSave}
               disabled={saving || saved}
               className={`flex-1 flex items-center justify-center px-4 py-3 rounded-lg border font-medium transition-all ${saved ? 'bg-green-50 border-green-200 text-green-700' : 'bg-gray-50 border-gray-200 hover:bg-gray-100'}`}
             >
               {saving ? <Loader2 className="animate-spin mr-2"/> : saved ? <Check className="mr-2"/> : <Save className="mr-2"/>}
               {saved ? 'Сохранено' : (saving ? 'Отправка...' : 'Отправить повторно')}
             </button>
             
             <button 
               onClick={() => downloadResultsAsJSON(data)}
               className="flex-1 flex items-center justify-center px-4 py-3 bg-gray-50 border border-gray-200 rounded-lg hover:bg-gray-100 font-medium"
             >
               <Download className="mr-2"/> Скачать JSON
             </button>
           </div>

           {/* Error and Manual Key Entry */}
           {saveError && (
             <div className="bg-red-50 border border-red-200 p-4 rounded-lg text-sm text-red-800 mt-4 animate-fade-in">
                <div className="flex items-center font-bold mb-1">
                  <AlertCircle size={16} className="mr-2"/> Ошибка сохранения
                </div>
                <p>{saveError}</p>
                
                {needsKey && (
                  <div className="mt-3">
                    <label className="block text-xs font-bold uppercase text-red-700 mb-1">
                      Введите Supabase Anon Key:
                    </label>
                    <div className="flex">
                      <input 
                        type="text" 
                        value={manualKey}
                        onChange={(e) => setManualKey(e.target.value)}
                        placeholder="eyJhbGciOiJIUzI1NiIsInR5..."
                        className="flex-1 border border-red-300 rounded-l-md px-3 py-2 text-xs focus:outline-none focus:ring-1 focus:ring-red-500"
                      />
                      <button 
                        onClick={handleManualSave}
                        className="bg-red-600 text-white px-3 py-2 rounded-r-md hover:bg-red-700"
                      >
                        <Save size={16} />
                      </button>
                    </div>
                    <p className="text-xs mt-1 text-red-500">Ключ можно найти в настройках проекта Supabase (API).</p>
                  </div>
                )}

                {(!needsKey && !saved) && (
                   <button 
                     onClick={() => setShowSqlHelp(!showSqlHelp)}
                     className="mt-2 text-blue-600 hover:text-blue-800 underline flex items-center text-xs font-semibold"
                   >
                     <Database size={14} className="mr-1"/> 
                     {showSqlHelp ? 'Скрыть инструкцию' : 'Как исправить ошибку базы данных?'}
                   </button>
                )}
             </div>
           )}
        </div>
      </div>

      {/* SQL Helper */}
      {showSqlHelp && (
        <div className="w-full bg-slate-800 text-slate-200 p-6 rounded-xl animate-fade-in border border-slate-700 relative shadow-2xl">
           <div className="absolute top-4 right-4">
             <button 
               onClick={copySql}
               className="flex items-center px-3 py-1.5 bg-slate-700 hover:bg-slate-600 rounded text-xs font-bold transition-colors border border-slate-600"
             >
               {copiedSql ? <Check size={14} className="mr-1 text-green-400"/> : <Copy size={14} className="mr-1"/>}
               {copiedSql ? 'Скопировано!' : 'Копировать SQL'}
             </button>
           </div>
           
           <h4 className="font-bold text-white mb-2 flex items-center text-lg">
             <Database className="mr-2 text-blue-400" /> Исправление ошибки доступа (RLS)
           </h4>
           <p className="text-sm text-slate-400 mb-4 max-w-2xl">
             Ошибка "42501" означает, что база данных защищена и не разрешает анонимную запись. 
             Чтобы разрешить сохранение результатов, выполните этот код в <a href="https://supabase.com/dashboard/project/_/sql" target="_blank" rel="noreferrer" className="text-blue-400 hover:underline hover:text-blue-300">Supabase SQL Editor</a>.
           </p>
           
           <div className="relative">
             <pre className="bg-black/50 p-4 rounded-lg text-xs font-mono overflow-x-auto border border-slate-900/50 text-green-300">
               {sqlCode}
             </pre>
           </div>
        </div>
      )}

      {/* AI Analysis Result */}
      {aiAnalysis && (
        <div className="w-full bg-indigo-50 border border-indigo-100 p-6 rounded-xl animate-fade-in">
           <div className="flex items-center mb-2">
              <BrainCircuit className="text-indigo-600 mr-2" size={20} />
              <h4 className="font-bold text-indigo-900">Анализ Gemini</h4>
           </div>
           <p className="text-indigo-800 leading-relaxed">
             {aiAnalysis}
           </p>
        </div>
      )}

      <button 
        onClick={onRestart}
        className="mt-8 text-gray-500 hover:text-gray-800 flex items-center"
      >
        <RefreshCw className="mr-2" size={16} /> Начать заново
      </button>
    </div>
  );
};

export default Results;